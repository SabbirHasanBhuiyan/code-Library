const int mx=2001;
string digit[]={"1110111", "0010010", "1011101", "1011011", "0111010", "1101011", "1101111", "1010010", "1111111", "1111011"};
string num[mx];
int n,k,dp[mx][mx];
struct info{
    int i,c,ans;
    info(){}
    info(int i,int c,int ans) : i(i),c(c),ans(ans) {}
}dir[mx][mx];

int getVal(string &s1,string &s2)
{
    int cost=0;
    for(int i=0;i<s1.size();i++){
        if(s1[i]=='1' && s2[i]=='0')    return -1;
        else if(s1[i]=='0' && s2[i]=='1') cost++;
    }
    return cost;
}

bool solve(int i,int k)
{
    if(i>n) return k==0;
    if(dp[i][k]!=-1)    return dp[i][k];

    int cost;
    for(int j=9;j>=0;j--){
        cost=getVal(num[i],digit[j]);
        if(cost==-1)    continue;

        if(cost<=k){
            if(solve(i+1,k-cost)){
                dir[i][k]=info(i+1,k-cost,j);
                return dp[i][k]=1;
            }
        }
    }
    return dp[i][k]=0;
}

void print(int i,int k)
{
    if(i==(n+1))    return;
    cout<<dir[i][k].ans;
    print(dir[i][k].i,dir[i][k].c);
}
